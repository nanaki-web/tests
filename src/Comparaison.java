public class Comparaison {
    public static void main (String args[])
    {
        int nX = 7, nY = 11, nZ = 11 ;
        System.out.println(nX) ;
        System.out.println(nY) ;
        System.out.println(nX < nY) ;
        System.out.println(nX > nZ) ;
        System.out.println(nY <= nZ) ;
        System.out.println(nX >= nY) ;
        System.out.println(nY == nZ) ;
        System.out.println(nX != nZ) ;
    }
}
