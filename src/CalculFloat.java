public class CalculFloat {
    public static void main (String args[])
    {
        float dX = 23.5F, dY = 7.3F ;
        System.out.println(dX) ;
        System.out.println(dY) ;
        System.out.println(dX + dY) ;
        System.out.println(dX - dY) ;
        System.out.println(dX * dY) ;
        System.out.println(dX / dY) ;
        System.out.println(dX % dY) ;
    }
}
