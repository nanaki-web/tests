public class CalculerEntier {
    public static void main(String[] args) {
        int nX = 17, nY = 5 ;
        System.out.println(nX) ;
        System.out.println(nY) ;
        System.out.println(nX + nY) ;
        System.out.println(nX - nY) ;
        System.out.println(nX * nY) ;
        System.out.println(nX / nY) ;
        System.out.println(nX % nY) ;
    }
}
